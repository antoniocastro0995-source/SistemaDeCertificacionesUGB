-- ==========================================
-- SUPABASE POSTGRESQL SCHEMA
-- Cópialo y pégalo en el "SQL Editor" de Supabase
-- ==========================================

-- 1. Usuarios
CREATE TABLE IF NOT EXISTS public.users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT,
    username TEXT UNIQUE,
    password TEXT,
    rol TEXT CHECK (rol IN ('super_admin', 'admin', 'docente')),
    estado_password TEXT DEFAULT 'temporal',
    avatar_url TEXT DEFAULT '',
    telefono TEXT DEFAULT '',
    is_private BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. Progreso de Módulos
CREATE TABLE IF NOT EXISTS public.progress (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES public.users(id) ON DELETE CASCADE,
    module_1_progress INTEGER DEFAULT 0,
    module_2_progress INTEGER DEFAULT 0,
    module_3_progress INTEGER DEFAULT 0,
    module_4_progress INTEGER DEFAULT 0,
    total_progress INTEGER DEFAULT 0,
    is_certified BOOLEAN DEFAULT false
);

-- 3. Recursos de Módulos (Materiales de Clase)
CREATE TABLE IF NOT EXISTS public.module_resources (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    module_id INTEGER,
    week_id INTEGER,
    title TEXT,
    type TEXT,
    url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 4. Configuración de Exámenes (Fechas de apertura/cierre)
CREATE TABLE IF NOT EXISTS public.exams_config (
    module_id INTEGER PRIMARY KEY,
    start_time TIMESTAMP WITH TIME ZONE,
    end_time TIMESTAMP WITH TIME ZONE
);

-- 5. Intentos de Exámenes
CREATE TABLE IF NOT EXISTS public.exam_attempts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES public.users(id) ON DELETE CASCADE,
    module_id INTEGER,
    attempts INTEGER DEFAULT 0,
    grade REAL DEFAULT 0,
    is_passed BOOLEAN DEFAULT false,
    status TEXT DEFAULT 'en_progreso', -- 'en_progreso', 'pendiente_revision', 'calificado'
    ai_flag BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 6. Preguntas (Constructor de Evaluaciones)
CREATE TABLE IF NOT EXISTS public.questions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    module_id INTEGER,
    type TEXT, -- 'multiple', 'tf', 'link'
    text TEXT,
    options_json TEXT, -- JSON array
    correct_answer TEXT,
    image_url TEXT,
    points REAL DEFAULT 1
);

-- 7. Respuestas del Estudiante
CREATE TABLE IF NOT EXISTS public.user_answers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    attempt_id BIGINT REFERENCES public.exam_attempts(id) ON DELETE CASCADE,
    question_id BIGINT REFERENCES public.questions(id) ON DELETE CASCADE,
    student_answer TEXT,
    file_url TEXT,
    points_awarded REAL DEFAULT 0
);

-- 8. Registro de Auditoría (Audit Logs)
CREATE TABLE IF NOT EXISTS public.audit_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    admin_id BIGINT,
    admin_name TEXT,
    action TEXT,
    details_json TEXT,
    timestamp TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

