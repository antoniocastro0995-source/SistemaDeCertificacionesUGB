-- ==========================================
-- DESHABILITAR RLS (Row Level Security)
-- Al ser un proyecto "Serverless" donde el front-end hace las consultas
-- directamente, necesitamos que las tablas sean públicas.
-- ==========================================
ALTER TABLE public.users DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.progress DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.module_resources DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.exams_config DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.exam_attempts DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.questions DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.user_answers DISABLE ROW LEVEL SECURITY;
ALTER TABLE public.audit_logs DISABLE ROW LEVEL SECURITY;

-- ==========================================
-- HABILITAR REALTIME (WebSockets)
-- Permite que los cambios caigan "silenciosamente" y en vivo.
-- ==========================================
BEGIN;
  DROP PUBLICATION IF EXISTS supabase_realtime;
  CREATE PUBLICATION supabase_realtime FOR TABLE 
    public.users,
    public.progress,
    public.exams_config,
    public.exam_attempts;
COMMIT;

